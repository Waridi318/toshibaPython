# arithmetic operators

x = 7
y = 3

print(x + y)
print(x - y)
print(x * y)
print(x / y)  # divide
print(x % y)  # remainder

# Comparison operators

a = 12
b = 34

print(f"{a} is not equal to variable {b}")
print(f"{a} is not equal to {b} {a != b}")
print(f"{a} is great than {b} {a > b}")
print(f"{a} is great than or equals to {b} {a >= b}")
print(f"{a} is less than {b} {a < b}")
print(f"{a} is less than or equal to {b} {a <= b}")

# Logical operators

c = 5
print(f"is this statement True {2 < c < 5}")
print(f"one of the condition is true {c > 2 or c < 5}")
