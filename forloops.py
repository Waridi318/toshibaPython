# Loops used in Data Stratures and Algorithyms
cars = ["Nissan", "Toyota", "Mercedes", "Range Rover", "VW"]

for i in cars:
    print(i)
    if i == "Toyota":
        break

for x in range(2, 6):
    print(x)

color = ["red", "big", "tasty"]
fruits = ["Apples", "Oranges", "Cherry"]

for rangi in color:
    for matunda in fruits:
        print(rangi, matunda)
